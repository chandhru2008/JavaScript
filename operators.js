// Operators are used to perform operations on variables and values.

// Arithmetic operators: +, -, *, /, % (addition, subtraction, multiplication, division, modulus)
let a = 10;
let b = 24;
let additionOperator = a + b;
let subtractionOperator = a - b;
let multiplicationOperator = a * b;
let divisionOperator = a / b;
let modulusOperator = a % b;
console.log(additionOperator)
console.log(subtractionOperator)
console.log(multiplicationOperator)
console.log(divisionOperator)
console.log(modulusOperator)
// Assignment operators: =, +=, -=, etc.
let c = 10; // Here = is a Assignment operator
c+=10; // add 10;
c-=10; // subtraction  10;
console.log(c);


// Comparison operators: ==, ===, !=, >, <, >=, <= (return true or false to use in if else)

console.log(a==b) // Check to values or equal
console.log(a===b) // Check to values and data type
console.log(a!=b) // True if both or not same
console.log(a>b) // Check a is greter then b
